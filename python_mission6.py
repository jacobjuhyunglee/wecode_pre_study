# list method 중 append, pop, sort 을 활용한 함수를 작성해보세요.

my_wishes_in_2021 = ['weight-loss', 'romance', 'get hired', 'new car']

#append로 맨 오른쪽에 제주도 여행 가기 추
my_wishes_in_2021.append('trip to Jeju') # 맨 오른쪽에 요소 추가

#pop가으로 다양한 요소 삭제
my_wishes_in_2021.pop() # 맨 오른쪽 요소 삭제

my_wishes_in_2021.pop(0) # 0번째 요소 삭제기

#sort로 오름차순 내림차순 정렬하
my_wishes_in_2021.sort() # 알파벳 순으로 정렬

my_wishes_in_2021.sort(reverse=True) # 알파벳 거꾸로 정렬
